# Historia:
# En un bosque encantado, los animales decidieron organizar una fiesta.
# Todos estaban emocionados, pero había un problema:
# ¿cómo calcular la cantidad de comida que necesitaban para todos los invitados?
# El Conejo Codificador, famoso por su habilidad con los números, decidió ayudar.

# Datos
numero_de_animales = 5  # Número de animales en la fiesta
comida_por_animal = 1.5  # Cantidad de comida por animal en kg

# Cálculo de la comida total necesaria
comida_total = numero_de_animales * comida_por_animal

# Resultado
print("La cantidad total de comida necesaria es: " + str(comida_total) + "kg")
