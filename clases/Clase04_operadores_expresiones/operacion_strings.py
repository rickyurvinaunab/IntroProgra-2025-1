#Operador concatenación
mensaje1 = 'Hola' + ' ' + 'Mundo'
print(mensaje1)
# Hola Mundo

#Operador multiplicación
mensaje2a = 'Hola ' * 3
mensaje2b = 'Mundo'
print(mensaje2a + mensaje2b)
# Hola Hola Hola Mundo

#Operador para añadir
mensaje3 = 'Hola'
mensaje3 += ' '
mensaje3 += 'Mundo'
print(mensaje3)
#Hola Mundo

#Sumar 1 a una variable
numero1 = 2
numero1 = numero1 + 1
print(numero1)
#3

#Operador para añadir con variables numéricas
numero1 = 2
numero1 += 1
print(numero1)
#3

