#Transformación de entero a flotante
numero_entero = 10
print(numero_entero)
# 10
numero_flotante = float(numero_entero)
print(numero_flotante)
# 10.0

#Transformación de flotante a entero
numero_flotante = 10.6
print(numero_flotante)
#10.6
numero_entero = int(numero_flotante)
print(numero_entero)
#10

#Redonde de números
numero_flotante = 10.6
print(numero_flotante)
#10.6
numero_entero = round(numero_flotante)
print(numero_entero)
#11


