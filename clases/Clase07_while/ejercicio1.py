# Ejercicio 1: Cuenta Ascendente
# Escribe un programa que utilice un bucle while para contar de 1 a 10 e imprima cada número.

# Inicializar la variable contador
contador = 1
# Iniciar el bucle while
while contador <= 10:
    # Imprimir el número
    print(contador)
    # Incrementar el contador
    contador += 1

# Imprimir mensaje de fin
print("Fin del programa")