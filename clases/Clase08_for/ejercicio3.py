# Ejercicio 3: Suma de Elementos de una Lista
# Escribe un programa que utilice un bucle for para sumar todos los elementos de una lista de números enteros dada.

# Inicializar la lista de números
numeros = [1, 2, 3, 4, 5]
# Inicializar la variable suma
suma = 0
# Iniciar el bucle for
for numero in numeros:
    # Sumar el número a la suma
    suma += numero
# Imprimir la suma
print("La suma de los elementos de la lista es: ", suma)
